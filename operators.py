# arithmetic operators
print("Arithmetic Operator")
print(10 + 5) #addition
print(10 - 5) #substraction
print(10 / 5) #division
print(4 * 5) #multiplication
print(10%3) #modulus
print(2 ** 4) #exponentiation
print( 4 // 3) #floor

print("-----------------")

#assignment operators
print("Assignment Operator")
x = 5
x += 3 # same as x = x + 3
print(x)
x -= 3 # same as x = x - 3
print(x)

print("-----------------")

#logical operators "output will true or false"
print("Logical Operator")
print(5 == 3) #equal operator
print(5 != 4) #not-equal operator
print(6 > 5) #greater than
print(4 >= 3) #greater than or equal to
print(6 < 5) #less than
print(4 <= 3) #less than or equal to

print("-----------------")

# Bitwise operator
print("Bitwise Operator")
print(1&0) #AND
print(1|0) #OR
print(1^1) #XOR


